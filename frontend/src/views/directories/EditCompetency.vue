<template>
  <Menu>
    <DirectoryDetail :object="data.currentObject" :title="this.$t('Edit competency')" name="editCompetency" :errorMessage="data.errorMessage" :on-clicked="editObject"/>
  </Menu>
</template>

<script>
import Menu from '../../layouts/Menu'
import DirectoryDetail from '../../components/directories/DirectoryDetail'
import { onGetSingle, onPutSingle } from '../../api/requests'

export default {
  name: 'EditCompetency',
  data () {
    return {
      data: {
        currentObject: {},
        loading: false,
        errorMessage: ''
      }
    }
  },
  mounted: function () {
    this.data.currentObject.id = this.$route.params.id
    this.getObject()
  },
  methods: {
    getObject: function () {
      onGetSingle('/api/competency/', 'currentObject', this.data)
    },
    editObject: function () {
      onPutSingle('/api/competency/', this.data, this.getObject)
    }
  },
  components: {
    Menu,
    DirectoryDetail
  }
}
</script>

